    public void tampilkanData(){
        String[] coloumn = {"Product ID", "Product Name", "Category", "Price"};
        ArrayList<Product> list = new Product().getAll();
        Object rowData[] = new Object[4];
        
        tblProductData.setModel(new DefaultTableModel(new Object[][] {}, coloumn));
        for(Product pr : list){
            rowData[0] = pr.getIdBarang();
            rowData[1] = pr.getNamaBarang();
            rowData[2] = pr.getKategori();
            rowData[3] = pr.getHarga();
            
            ((DefaultTableModel)tblProductData.getModel()).addRow(rowData);
        }
    }
    
    public void cari(String keyword){
        String[] coloumn = {"Product ID", "Product Name", "Category", "Price"};
        ArrayList<Product> list = new Product().search(keyword);
        Object rowData[] = new Object[4];
        
        tblProductData.setModel(new DefaultTableModel(new Object[][] {}, coloumn));
        for(Product pr : list){
            rowData[0] = pr.getIdBarang();
            rowData[1] = pr.getNamaBarang();
            rowData[2] = pr.getKategori();
            rowData[3] = pr.getHarga();
            
            ((DefaultTableModel)tblProductData.getModel()).addRow(rowData);
        }
    }

