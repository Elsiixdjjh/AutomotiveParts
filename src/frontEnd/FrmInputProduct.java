    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tblProductData.getModel();
        int row = tblProductData.getSelectedRow();
        
        Product pr = new Product().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        pr.delete();
        kosongkanForm();
        JOptionPane.showMessageDialog(null, "Customer data has been successfully deleted");
        tampilkanData();
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tblProductData.getModel();
        int row = tblProductData.getSelectedRow();
        
        String id = txtProductId.getText();
        String name = txtProductName.getText();
        String kategori = (String) cbCategory.getSelectedItem();
        String harga = txtPrice.getText();
        
        String sql = "UPDATE barang SET namaBarang = '" +name+ "', kategori = '" + kategori + "', hargaBarang = '" + harga + "' WHERE idBarang = '" +id+ "'";
        Database.executeQuery(sql);
        JOptionPane.showMessageDialog(null, "Product data successfully updated");
        tampilkanData();
    }//GEN-LAST:event_btnUpdateActionPerformed

