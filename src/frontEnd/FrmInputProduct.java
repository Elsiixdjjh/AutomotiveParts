    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        Product pr = new Product();
        pr.setIdBarang(Integer.parseInt(txtProductId.getText()));
        pr.setNamaBarang(txtProductName.getText());
        pr.setKategori((String) cbCategory.getSelectedItem());
        pr.setHarga(Integer.parseInt(txtPrice.getText()));
        pr.save();
        
        txtProductId.setText(Integer.toString(pr.getIdBarang()));
        JOptionPane.showMessageDialog(null, "Product data successfully added");
        tampilkanData();
    }//GEN-LAST:event_btnAddActionPerformed

